int	my_get_num(char str)
{
  if (str == '0')
    return 0;
  if (str == '1')
    return 1;
  if (str == '2')
    return 2;
  if (str == '3')
    return 3;
  if (str == '4')
    return 4;
  if (str == '5')
    return 5;
  if (str == '6')
    return 6;
  if (str == '7')
    return 7;
  if (str == '8')
    return 8;
  if (str == '9')
    return 9;
}
